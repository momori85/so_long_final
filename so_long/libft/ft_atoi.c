#include "libft.h"

int	ft_atoi(const char *str)
{
	int	result;
	int	minus;

	minus = 1;
	result = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			minus = -1;
		str++;
	}
	while (ft_isdigit(*str))
		result = (result * 10) + (*str++ - 48);
	return (result * minus);
}
